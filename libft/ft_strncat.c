#include <string.h>

char		*ft_strncat(char *s1, const char *s2, size_t n)
{
	char		*i;

	i = (char *)s1;
	while (*i)
		i++;
	while (*s2 && n--)
		*i++ = *s2++;
	*i = 0;
	return (s1);
}
